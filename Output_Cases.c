#include<stdio.h>

int main() {
    
    int age = 17;
    float pi = 3.14;
    char star = '*';
    
    //for printing values of int with something
    printf("my age is %d \n" , age);

    //for printing values of float with something
    printf("value of pi is %f \n" , pi);

    //for printing values of char with something
    printf("star looks like %c \n" , star);

    return 0;
}